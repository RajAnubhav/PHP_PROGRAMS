<?php
    // switch statement in php
$age = 12;
    switch($age){
        case '12':
            echo "You are 12 years old";
            break;
        case 42:
            echo "You are 42 years old";
            break;
        case 23:
            echo "You are 23 years old";
            break;
        default:
            echo "Your age is not present";
            break;
        }
?>